let locale = localStorage.getItem('nyan-split-locale') || 'zh';

export function t(key, values = {}) {
  const message = messages[locale][key] || key;
  return String(message).replace(/\{(\w+)\}/g, (match, name) => (
    values[name] === undefined || values[name] === null ? match : String(values[name])
  ));
}

export function getLocale() {
  return locale;
}

function applyDocumentLocale() {
  document.documentElement.lang = locale === 'zh' ? 'zh-Hant' : 'en';
  document.title = t('siteTitle');
  document.querySelector('meta[name="description"]')?.setAttribute('content', t('siteDescription'));
}

export function toggleLocale() {
  locale = locale === 'en' ? 'zh' : 'en';
  localStorage.setItem('nyan-split-locale', locale);
  applyDocumentLocale();
  return locale;
}

applyDocumentLocale();
